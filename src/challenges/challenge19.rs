pub fn main() {
    let cipher_texts: Vec<Vec<u8>> = CIPHER_TEXTS.iter().map(|&txt| bin_utils::base64_to_bytes(txt.to_string())).collect();
    let size = cipher_texts.iter().map(|txt| txt.len()).min().unwrap();
    //let chunks: Vec<Vec<u8>> = cipher_texts.chunks(size).cloned().collect();
    let mut trimmed = Vec::new();
    for mut text in cipher_texts {
        text.truncate(size);
        trimmed.push(text);
    }
    let transposed = bin_utils::transpose(trimmed);
    let trans_result: Vec<Vec<u8>> = transposed.iter().map(|chunk| {
        let best_byte = (0..u8::max_value()).max_by_key(|&byte| (char_frequency::score_string(
            &bin_utils::xor_buffers(&chunk, &vec![byte; size])) * 1000.0) as i32).unwrap();
        chunk.iter().map(|&b| b ^ best_byte).collect()
    }).collect();
    let final_result = bin_utils::transpose(trans_result);
    for line in final_result.iter() {
        println!("{}", bin_utils::bytes_to_ascii(&line));
    }

}
